package com.nahida.arraydemo;

public class Arraydemo {

    public static void main(String[] args) {
        int[] arr1 = {11, 12, 13, 14};
        double[] arr3 = {1.93, 1.75, 1.73, 1.81};
        for (int arr12 = 0; arr12 < arr1.length; arr12++) {
            System.out.println(arr1[arr12]);
        }
        for (int arr32 = 0; arr32 < arr3.length; arr32++) {
            System.out.println(arr3[arr32]);
        }
    }
}
