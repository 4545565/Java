package com.nahida.arith;

public class Arith {

    public static void main(String[] args) {
        System.out.println("Hello world!");
        System.out.println(0.1 + 0.2);
        System.out.println(10.0 / 3);
        System.out.println(10.0 % 3.0);
        int i = 1;
        System.out.println(i);
        i = (++i) + (++i);
        System.out.println(i);
    }
}
